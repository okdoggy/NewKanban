import { parseDate } from "@/lib/date-utils";
import type { CalendarViewMode, GanttZoom, LanguageCode, MemberRole, TaskPriority, TaskStatus, ViewKey } from "@/lib/types";

export function normalizeLocale(locale: string | null | undefined): LanguageCode {
  return locale === "ko" ? "ko" : "en";
}

export function localeTag(locale: LanguageCode) {
  return locale === "ko" ? "ko-KR" : "en-US";
}

export function formatLocaleDate(locale: LanguageCode, date: Date, options: Intl.DateTimeFormatOptions) {
  return new Intl.DateTimeFormat(localeTag(locale), options).format(date);
}

export function formatLocaleDateValue(locale: LanguageCode, value: string, options: Intl.DateTimeFormatOptions) {
  return formatLocaleDate(locale, parseDate(value), options);
}

