export function getWorkspaceText(locale: LanguageCode) {
  return messages[normalizeLocale(locale)];
}

export function getViewLabel(locale: LanguageCode, view: ViewKey) {
  const text = getWorkspaceText(locale);
  const labels: Record<ViewKey, string> = {
    home: text.home,
    inbox: text.inbox,
    "my-work": text.myWork,
    projects: text.projects,
    calendar: text.calendar,
    collaborate: text.collaborate,
  };
  return labels[view];
}

